#!/usr/bin/env python3
"""Emit a USENIX-style LaTeX table from paper-eval-4/5 JSON (aggregate_by_defense + manifest)."""
from __future__ import annotations

import argparse
import json
import statistics
from pathlib import Path
from typing import Any, Callable, Dict, List, Optional, Tuple


def _collect(
    all_runs: List[Dict[str, Any]],
    defense: str,
    scenario_id: str,
    key: str,
    cast: Callable[[Any], Any] = float,
) -> List[Any]:
    vals: List[Any] = []
    for run in all_runs:
        for row in run["results"]:
            if row.get("defense_baseline") != defense or row.get("scenario_id") != scenario_id:
                continue
            if key not in row or row[key] is None:
                continue
            vals.append(cast(row[key]))
    return vals


def per_seed_stats(all_runs: List[Dict[str, Any]], defense: str) -> Dict[str, Tuple[Optional[float], Optional[float]]]:
    """(mean, stdev) per metric; stdev None if <2 values."""

    def ms(vals: List[float]) -> Tuple[Optional[float], Optional[float]]:
        if not vals:
            return None, None
        if len(vals) == 1:
            return vals[0], None
        return statistics.mean(vals), statistics.stdev(vals)

    out: Dict[str, Tuple[Optional[float], Optional[float]]] = {}
    f1s = _collect(all_runs, defense, "extraction_leak", "extraction_f1")
    out["ext_f1"] = ms([float(x) for x in f1s])
    rsrs = _collect(all_runs, defense, "refusal_keyword", "rsr", cast=bool)
    out["kw_rsr"] = ms([1.0 if x else 0.0 for x in rsrs])
    util = _collect(all_runs, defense, "benign_baseline", "rsr", cast=bool)
    out["ben_util"] = ms([0.0 if x else 1.0 for x in util])
    impr = _collect(all_runs, defense, "benign_fpr_suite", "improper_refusal_rate")
    out["ben_fpr"] = ms([float(x) for x in impr])
    hpm = _collect(all_runs, defense, "hpm_proxy", "hpm_rsr_rate")
    out["hpm"] = ms([float(x) for x in hpm])
    hrm = _collect(all_runs, defense, "harmful_rsr_suite", "harmful_rsr_rate")
    out["harm"] = ms([float(x) for x in hrm])
    mf1 = _collect(all_runs, defense, "multi_round_extraction", "max_f1")
    out["mr_f1"] = ms([float(x) for x in mf1])
    return out


def per_seed_secondary_stats(
    all_runs: List[Dict[str, Any]], defense: str, suffix: str
) -> Dict[str, Tuple[Optional[float], Optional[float]]]:
    """Means over seeds for relabeled rates (relabel_tracka_llama_guard.py)."""

    def ms(vals: List[float]) -> Tuple[Optional[float], Optional[float]]:
        if not vals:
            return None, None
        if len(vals) == 1:
            return vals[0], None
        return statistics.mean(vals), statistics.stdev(vals)

    sk = suffix.strip()
    out: Dict[str, Tuple[Optional[float], Optional[float]]] = {}
    pairs = [
        ("harm_sec", "harmful_rsr_suite", f"harmful_rsr_rate_{sk}"),
        ("wild_sec", "wild_rsr_suite", f"wild_rsr_rate_{sk}"),
        ("sr_sec", "strongreject_rsr_suite", f"strongreject_rsr_rate_{sk}"),
        ("ben_sec", "benign_fpr_suite", f"improper_refusal_rate_{sk}"),
        ("hpm_sec", "hpm_proxy", f"hpm_rsr_rate_{sk}"),
    ]
    for key, scen, rk in pairs:
        raw = _collect(all_runs, defense, scen, rk)
        out[key] = ms([float(x) for x in raw])
    return out


def fmt(x: Optional[float], digits: int = 3) -> str:
    if x is None:
        return "---"
    return f"{x:.{digits}f}"


def fmt_pm(m: Optional[float], s: Optional[float], digits: int = 3) -> str:
    if m is None:
        return "---"
    if s is None:
        return fmt(m, digits)
    return f"{m:.{digits}f} $\\pm$ {s:.{digits}f}"


def emit_table(
    doc: Dict[str, Any],
    *,
    language: str,
    variant: str = "main",
    secondary_suffix: str = "",
) -> str:
    man = doc.get("manifest", {})
    defs: List[str] = doc.get("defenses", [])
    runs = doc.get("runs", [])
    proto = man.get("protocol_version", "")
    model = man.get("openai_model_field", "")
    up = man.get("upstream_url", "")
    guard_on = bool(man.get("gateway_output_guard_header"))

    lines = [
        "% Auto-generated by export_trackA_table_latex.py",
        f"% protocol={proto} model={model} upstream={up} variant={variant} gateway_output_guard_header={guard_on}",
        r"\begin{table*}[t]",
        r"\centering",
        r"\small",
        r"\setlength{\tabcolsep}{3pt}",
    ]
    if variant == "guard":
        if language == "en":
            cap = (
                r"\caption{\textbf{Track~A output-guard ablation} (protocol \texttt{"
                + str(proto).replace("_", r"\_")
                + r"}; \texttt{manifest.gateway\_output\_guard\_header=true}). "
                r"Same column semantics as Table~\protect\ref{tab:tracka-main}; non-\texttt{direct\_upstream} defenses send "
                r"\texttt{X-Gateway-Output-Guard:1} when the gateway is configured with "
                r"\texttt{GATEWAY\_OUTPUT\_GUARD\_URL}. "
                r"If the archived JSON used a reduced \texttt{scenarios} list, cells not present in that run appear as ---. "
                r"Heuristic refusal judge.}"
            )
            lbl = r"\label{tab:tracka-guard}"
            hdr = (
                r"\begin{tabular}{@{}lccccccc@{}}"
                r"\toprule"
                r"\textbf{Defense} & \textbf{Harm.\ RSR} & \textbf{Kw.\ RSR} & \textbf{Ext.\ $F_1$} & "
                r"\textbf{MR max $F_1$} & \textbf{HPM RSR} & \textbf{Ben.\ FPR} & \textbf{Ben.\ util.} \\"
                r"\midrule"
            )
        else:
            cap = (
                r"\caption{\textbf{Track~A 输出守卫消融}（协议 \texttt{"
                + str(proto).replace("_", r"\_")
                + r"}；\texttt{manifest.gateway\_output\_guard\_header=true}）。"
                r"列语义与表~\protect\ref{tab:tracka-main-cn} 相同；非 \texttt{direct\_upstream} 路径在网关已配置 "
                r"\texttt{GATEWAY\_OUTPUT\_GUARD\_URL} 时发送 \texttt{X-Gateway-Output-Guard:1}。"
                r"若归档 JSON 仅含部分场景，则未跑到的列显示为 ---。启发式拒绝判定。}"
            )
            lbl = r"\label{tab:tracka-guard-cn}"
            hdr = (
                r"\begin{tabular}{@{}lccccccc@{}}"
                r"\toprule"
                r"\textbf{防御基线} & \textbf{有害RSR} & \textbf{关键词RSR} & \textbf{抽取$F_1$} & "
                r"\textbf{多轮$F_1$} & \textbf{HPM RSR} & \textbf{良性FPR} & \textbf{良性效用} \\"
                r"\midrule"
            )
    elif language == "en":
        cap = (
            r"\caption{\textbf{Track~A reference gateway results} (protocol \texttt{"
            + str(proto).replace("_", r"\_")
            + r"}). Means with $\pm$ std.\ over random seeds when $n\ge 2$ per cell; "
            r"heuristic refusal judge. Columns: harmful single-turn RSR; keyword-scenario RSR; "
            r"extraction $F_1$; multi-round max $F_1$; HPM-proxy RSR; benign improper-refusal rate; "
            r"benign utility (fraction non-refused on a trivial task).}"
        )
        lbl = r"\label{tab:tracka-main}"
        hdr = (
            r"\begin{tabular}{@{}lccccccc@{}}"
            r"\toprule"
            r"\textbf{Defense} & \textbf{Harm.\ RSR} & \textbf{Kw.\ RSR} & \textbf{Ext.\ $F_1$} & "
            r"\textbf{MR max $F_1$} & \textbf{HPM RSR} & \textbf{Ben.\ FPR} & \textbf{Ben.\ util.} \\"
            r"\midrule"
        )
    else:
        cap = (
            r"\caption{\textbf{Track~A 参考网关实测}（协议 \texttt{"
            + str(proto).replace("_", r"\_")
            + r"}）。表中为跨随机种子的均值，$n\ge 2$ 时给出 $\pm$ 标准差；拒绝判定为启发式。"
            r"列：有害单轮 RSR；关键词场景 RSR；抽取 $F_1$；多轮最大 $F_1$；HPM 代理 RSR；"
            r"良性不当拒答率；良性效用（简单任务非拒答比例）。}"
        )
        lbl = r"\label{tab:tracka-main-cn}"
        hdr = (
            r"\begin{tabular}{@{}lccccccc@{}}"
            r"\toprule"
            r"\textbf{防御基线} & \textbf{有害RSR} & \textbf{关键词RSR} & \textbf{抽取$F_1$} & "
            r"\textbf{多轮$F_1$} & \textbf{HPM RSR} & \textbf{良性FPR} & \textbf{良性效用} \\"
            r"\midrule"
        )

    lines.extend([cap, lbl, hdr])

    for d in defs:
        st = per_seed_stats(runs, d)
        row = (
            f"\\texttt{{{d.replace('_', r'\_')}}} "
            f"& {fmt_pm(*st['harm'])} & {fmt_pm(*st['kw_rsr'])} & {fmt_pm(*st['ext_f1'])} "
            f"& {fmt_pm(*st['mr_f1'])} & {fmt_pm(*st['hpm'])} & {fmt_pm(*st['ben_fpr'])} "
            f"& {fmt_pm(*st['ben_util'])} \\\\"
        )
        lines.append(row)

    lines.extend([r"\bottomrule", r"\end{tabular}", r"\end{table*}"])

    if secondary_suffix:
        lines.append("")
        lines.extend(
            _emit_secondary_table(
                doc,
                language=language,
                defs=defs,
                runs=runs,
                proto=str(proto),
                model=str(model),
                suffix=secondary_suffix.strip(),
            )
        )

    return "\n".join(lines) + "\n"


def _emit_secondary_table(
    doc: Dict[str, Any],
    *,
    language: str,
    defs: List[str],
    runs: List[Dict[str, Any]],
    proto: str,
    model: str,
    suffix: str,
) -> List[str]:
    man = doc.get("manifest", {}) or {}
    rel = man.get("secondary_judge_relabel") or {}
    rev = str(rel.get("judge_model_revision") or man.get("eval_judge_chat", {}).get("judge_model_revision") or suffix)
    lines = [
        r"\begin{table*}[t]",
        r"\centering",
        r"\small",
        r"\setlength{\tabcolsep}{3pt}",
    ]
    esc = proto.replace("_", r"\_")
    rev_esc = rev.replace("_", r"\_")
    if language == "en":
        cap = (
            r"\caption{\textbf{Track~A secondary judge (re-labeled completions)} "
            r"(protocol \texttt{" + esc + r"}; stored \texttt{user\_text}/\texttt{assistant\_text}; "
            r"judge suffix \texttt{" + suffix.replace("_", r"\_") + r"}; revision \texttt{" + rev_esc + r"}). "
            r"Same gateway completions as the primary Track~A table; rates recomputed via HTTP judge only.}"
        )
        lbl = r"\label{tab:tracka-secondary-" + suffix.replace("_", "") + "}"
        hdr = (
            r"\begin{tabular}{@{}lccccc@{}}"
            r"\toprule"
            r"\textbf{Defense} & \textbf{Harm.\ RSR} & \textbf{Wild RSR} & \textbf{SR RSR} & "
            r"\textbf{Ben.\ FPR} & \textbf{HPM RSR} \\"
            r"\midrule"
        )
    else:
        cap = (
            r"\caption{\textbf{Track~A 二次裁判（对存盘 completion 重标）}"
            r"（协议 \texttt{" + esc + r"}；依赖 \texttt{user\_text}/\texttt{assistant\_text}；"
            r"后缀 \texttt{" + suffix.replace("_", r"\_") + r"}；revision \texttt{" + rev_esc + r"}）。"
            r"与主表~\protect\ref{tab:tracka-main-cn} 为同一批网关完成体，仅经 HTTP 裁判重算比例。}"
        )
        lbl = r"\label{tab:tracka-secondary-cn-" + suffix.replace("_", "") + "}"
        hdr = (
            r"\begin{tabular}{@{}lccccc@{}}"
            r"\toprule"
            r"\textbf{防御} & \textbf{有害RSR} & \textbf{Wild RSR} & \textbf{SR RSR} & "
            r"\textbf{良性FPR} & \textbf{HPM RSR} \\"
            r"\midrule"
        )
    lines.extend(
        [
            f"% secondary judge table model={model} suffix={suffix}",
            cap,
            lbl,
            hdr,
        ]
    )
    for d in defs:
        st = per_seed_secondary_stats(runs, d, suffix)
        row = (
            f"\\texttt{{{d.replace('_', r'\_')}}} "
            f"& {fmt_pm(*st['harm_sec'])} & {fmt_pm(*st['wild_sec'])} & {fmt_pm(*st['sr_sec'])} "
            f"& {fmt_pm(*st['ben_sec'])} & {fmt_pm(*st['hpm_sec'])} \\\\"
        )
        lines.append(row)
    lines.extend([r"\bottomrule", r"\end{tabular}", r"\end{table*}"])
    return lines


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--json", required=True, help="path to paper-eval-4 results JSON")
    ap.add_argument("--out-en", required=True, help="output .tex (English caption/headers)")
    ap.add_argument("--out-cn", default="", help="optional second .tex (Chinese caption/headers)")
    ap.add_argument(
        "--variant",
        choices=("main", "guard"),
        default="main",
        help="main: Table tab:tracka-main; guard: output-guard ablation captions/labels",
    )
    ap.add_argument(
        "--secondary-suffix",
        default="",
        help="if set (e.g. llama_guard4), append appendix table for relabeled rates from relabel_tracka_llama_guard.py",
    )
    args = ap.parse_args()
    doc = json.loads(Path(args.json).read_text(encoding="utf-8"))
    Path(args.out_en).parent.mkdir(parents=True, exist_ok=True)
    Path(args.out_en).write_text(
        emit_table(doc, language="en", variant=args.variant, secondary_suffix=args.secondary_suffix),
        encoding="utf-8",
    )
    if args.out_cn:
        Path(args.out_cn).parent.mkdir(parents=True, exist_ok=True)
        Path(args.out_cn).write_text(
            emit_table(doc, language="cn", variant=args.variant, secondary_suffix=args.secondary_suffix),
            encoding="utf-8",
        )
    print(
        "wrote",
        args.out_en,
        args.out_cn or "(no cn)",
        f"variant={args.variant}",
        f"secondary_suffix={args.secondary_suffix or '(none)'}",
    )


if __name__ == "__main__":
    main()
