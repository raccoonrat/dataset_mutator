#!/usr/bin/env python3
"""
Materialize JailbreakBench JBB-Behaviors (100 misuse + 100 benign goals) as plain-line files
for paper-eval harmful_rsr_suite / benign_fpr_suite (Update_dataset_Guide §5.3(7)).

Primary source: Hugging Face datasets.load_dataset(..., "behaviors", split=harmful|benign).
Tries repo ids: JailbreakBench/JBB-Behaviors, dedeswim/JBB-Behaviors (legacy).

Requires: pip install -r experiments/requirements-benchmark.txt

Example:
  python3 experiments/scripts/fetch_jbb_behaviors.py \\
    --misuse-out experiments/data/jbb_misuse_100_en.txt \\
    --benign-out experiments/data/jbb_benign_100_en.txt
"""

from __future__ import annotations

import argparse
import sys


def _goals_from_hf() -> tuple[list[str], list[str]]:
    try:
        from datasets import load_dataset
    except ImportError as e:
        raise SystemExit(
            "Missing dependency: pip install -r experiments/requirements-benchmark.txt\n" + str(e)
        ) from e

    last_err: Exception | None = None
    for repo in ("JailbreakBench/JBB-Behaviors", "dedeswim/JBB-Behaviors"):
        for loader in (
            lambda r: (  # noqa: E731
                load_dataset(r, "behaviors", split="harmful"),
                load_dataset(r, "behaviors", split="benign"),
            ),
            lambda r: (
                load_dataset(r, split="harmful"),
                load_dataset(r, split="benign"),
            ),
        ):
            try:
                harmful, benign = loader(repo)
                hg = [str(x).strip() for x in harmful["Goal"]]
                bg = [str(x).strip() for x in benign["Goal"]]
                hg = [x for x in hg if x]
                bg = [x for x in bg if x]
                return hg, bg
            except Exception as e:  # noqa: BLE001
                last_err = e
                continue
    raise RuntimeError(f"Could not load JBB-Behaviors from HF; last error: {last_err!r}")


def main() -> None:
    ap = argparse.ArgumentParser(description="Fetch JBB-Behaviors misuse/benign goal lists")
    ap.add_argument(
        "--misuse-out",
        default="experiments/data/jbb_misuse_100_en.txt",
        help="one misuse goal per line (UTF-8)",
    )
    ap.add_argument(
        "--benign-out",
        default="experiments/data/jbb_benign_100_en.txt",
        help="one benign goal per line (UTF-8)",
    )
    args = ap.parse_args()

    misuse, benign = _goals_from_hf()
    hdr = (
        "# JBB-Behaviors (JailbreakBench) — Goal column; auto-generated by fetch_jbb_behaviors.py\n"
        f"# misuse_n={len(misuse)} benign_n={len(benign)}\n"
    )
    from pathlib import Path

    mp = Path(args.misuse_out)
    bp = Path(args.benign_out)
    mp.parent.mkdir(parents=True, exist_ok=True)
    bp.parent.mkdir(parents=True, exist_ok=True)
    mp.write_text(hdr + "\n".join(misuse) + "\n", encoding="utf-8")
    bp.write_text(hdr + "\n".join(benign) + "\n", encoding="utf-8")
    print(f"wrote {len(misuse)} misuse -> {mp}", file=sys.stderr)
    print(f"wrote {len(benign)} benign -> {bp}", file=sys.stderr)


if __name__ == "__main__":
    main()
